# Tuplas en python, las tuplas son con parentesis

# Definir una tupla:

frutas=("Naranja","Platano","Guayaba")


# Saber el largo de la tupla

print(len(frutas))

# Acceder a un elemento

print(frutas[0] )

# Navegación inversa

print(frutas[-2])


# Acceder a un rango

print(frutas[1:2])  #Sin incluir el último indice 


