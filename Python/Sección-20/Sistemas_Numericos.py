
# Sistemas Numéricos en python:



# Tenemos los diferentes sistemas de numeración: Como Decimal,Binario,Octal y Hexadecimal

#  Decimal
a=10
# Binario- 0b así se hace un número binario en python
a=0b1010 

# Octal - 0o así se hace un número octal en python
a=0o12

# Hexadecimal-0x así se hace un número hexadecimal en python
a=0xA

print(a)

# Converir un tipo entero, incluyendo la base

# Base decimal
a=int('23',10)
print(a)


# Base binario
a=int('10111',2)
print(a)


# Base octal
a=int('27',8)
print(a)

# Base Hexadecimal
a=int('17',16)
print(a)

# Base 5
a = int('342',5)
print(a)











